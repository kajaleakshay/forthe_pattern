package decrease_by_sir;

public class star1
{
	public static void main(String[] args) 
	{
		
		int star =5;
		for(int a=1;a<5;a++)// it is for the row
		{
			for(int i=1; i<=star;i++)//1,2,3,4,5,break--loop1
			{
				System.out.print("*");
			}
			System.out.println();
			star--;
					
		}
		
		
	}
	
	

}
