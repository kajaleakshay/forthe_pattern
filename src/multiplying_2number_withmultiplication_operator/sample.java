package multiplying_2number_withmultiplication_operator;

public class sample 
{
	// perform theoperation 10*20 with using operator
	
	public static void main(String[] args)
	{
		int num1=10;
		int num2=20;
		
		//to store the value after every loop
		int sum=0;
		for(int i=1;i<=num2;i++)
		{
			sum=sum+num1;
			System.out.println(sum);
			
		}
		
	}

}
