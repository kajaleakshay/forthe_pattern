package multiplying_2number_withmultiplication_operator;

public class example2_multiply_2_num_without_multiplicationOperator 
{
	public static void main(String[] args) 
	{
		int num1=50;
		int num2=4;
		
		int sum=0;         //150
		
		//                5<=4
		for(int i=1; i<=num1; i++)
		{
			sum=sum+num2;                  // 150 + 50 =200
		}
		
		System.out.println(sum);
		
		
		
	}

}